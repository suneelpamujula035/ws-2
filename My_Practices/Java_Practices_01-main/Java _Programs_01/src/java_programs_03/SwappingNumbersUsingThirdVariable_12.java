package java_programs_03;

public class SwappingNumbersUsingThirdVariable_12 {

	public static void main(String[] args) {

		int a = 6;
		int b = 15;
		int temp;
		System.out.println("Before swapping  a = "+a+" b = "+b);
		temp = a;
		a = b;
		b = temp;
		System.out.println("Before swapping  a = "+a+" b = "+b);
	}

}
